import numpy as np
import pandas as pd
from scipy.stats import skew,kurtosis
temperatures=[30.5,32.1,29.8,31.0,33.2,30.0,34.5,28.9,29.5,31.7]
sorted_temps=sorted(temperatures)
N=len(sorted_temps)
manual_mean=sum(sorted_temps)/N
if N%2==0:
    manual_median=(sorted_temps[N//2-1]+sorted_temps[N//2])/2
else:
    manual_median=sorted_temps[N//2]
manual_mode=[val for val in sorted_temps if sorted_temps.count(val)>1]
manual_mode=list[set(manual_mode)]if manual_mode else "NO mode"
manual_range=max(sorted_temps)-min(sorted_temps)
manual_var=sum((x-manual_mean) ** 2 for x in sorted_temps)/(N-1)
manual_std=manual_var ** 0.5
q1=np.percentile(temperatures,25)
q3=np.percentile(temperatures,75)
iqr=q3-q1
sk=skew(temperatures)
kurt=kurtosis(temperatures)
print("Mean:'",manual_mean)
print("Median: ",manual_median)
print("Mode: ",manual_mode)
print("Range: ",manual_range)
print("Variance: ",manual_var)
print("Q1 :",q1)
print("Q3 :",q3)
print("IQR :",iqr)
print("Skewness :",sk)
print("kurtosis :",kurt)
